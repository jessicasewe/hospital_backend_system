import mongoose, { Schema } from "mongoose";

const doctorNoteSchema = new Schema({
  doctorId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  patientId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  note: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const DoctorNote = mongoose.model("DoctorNote", doctorNoteSchema);
export default DoctorNote;
